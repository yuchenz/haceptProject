中国 鞋业 产量 居 世界 第一 位
新华社 广州 6月 8日 电 ( 黄 玫 谢 楚光 ) 在 国际 鞋 类 市场 中 , 中国 鞋业 产量 已 占 世界 总量 近 50% , 在 全球 鞋 类 贸易 中 , 中国 鞋 类 出口额 也 已 占 到 25% , 两者 均 居 世界 第一 位 。
这 是 记者 从 近日 在 广州 召开 的 首 届 中国 沿海 鞋 类 出口 战略 研讨会 上 了解 到 的 。
