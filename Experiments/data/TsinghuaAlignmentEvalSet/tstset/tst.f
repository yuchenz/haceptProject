依据 这项 已 遭 撤销 的 和解 协议 , 将 由 立场 超然 的 中间人 以 个案 方式 评估 受害者 所 受 的 伤害 , 对于 受到 最 严重 创伤 者 可 给予 多 达 一百万 美元 赔偿 , 而 受到 较 不 严重 伤害 的 受害者 则 给予 二万五千 美元 。
