中国 轻工业 进出口 总公司 有关 负责人 指出 , 目前 , 中国 同 类型 的 鞋 平均 价格 不仅 远远 低于 意大利 、 巴西 、 韩国 、 台湾 等 国家 和 地区 , 甚至 已经 低于 越南 等 国家 。
与此同时 , 出口 价格 还 有 下降 的 趋势 。
据悉 , 广东省 是 中国 鞋 类 产量 最 大 的 地区 , 目前 年产量 近 30亿 双 , 约 占 全世界 年产量 的 三分之一 。
但 广东 制鞋业 也 同样 面临 着 上述 问题 。
针对 存在 的 问题 , 专家 们 呼吁 , 中国 制鞋业 需要 进一步 提高 质量 档次 、 加快 产品 的 更新换代 、 努力 创立 自己 的 名牌 、 注重 拓展 外销 市场 。
