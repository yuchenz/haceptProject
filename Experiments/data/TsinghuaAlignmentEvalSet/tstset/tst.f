贺兰山 成为 中国 西北 动植物 基因 宝库
新华社 银川 6月 25日 电 ( 记者 姜 雪城 李 云路 ) 随着 生态 环境 不断 改善 , 贺兰山 国家级 自然保护区 已 成为 名副其实 的 " 中国 西北 地区 动植物 基因 宝库 " 。
